// Muestra los numero del 1 al 100 (ambos incluidos). Usa un bucle while
public class Ejercicio7 {

	public static void main(String[] args) {
		// Creamos las variables
		int num = 1;
		
		// Mientras el numero sea diferente 101 se repite
		while (num != 101) {			
			System.out.println(num);
			num++;
		}
	}
}